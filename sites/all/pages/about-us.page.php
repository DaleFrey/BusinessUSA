	    <p class="highlight">
	    On October 28, 2011, the President issued a challenge to 
	    government agencies to think beyond their organizational boundaries in the best interest
        of serving America's business community, and start thinking and acting more like the businesses they serve. <a href="http://www.whitehouse.gov/the-press-office/2011/10/28/presidential-memorandum-making-it-easier-americas-small-businesses-and-a" target="_blank"> Read more... </a>
	    </p>

	    <div class="left-column"> 
		    <p>He directed the creation of BusinessUSA, a centralized, one-stop platform to make it easier than ever for 
		    businesses to access services to help them grow and hire.</p>

		    <p>BusinessUSA implements a "no wrong door" approach for small businesses and exporters 
		    by using technology to quickly connect businesses to the services and information relevant 
		    to them, regardless of where the information is located or which agency's website, call center, 
		    or office they go to for help. Looking forward, the more federal agencies continue to add 
		    resources to BusinessUSA to encompass the full range of business programs and services, 
		    the more we will be able to reduce the confusing array of websites that exist today. To ensure 
		    that it is oriented towards the needs of the customer, BusinessUSA will be designed, tested, 
		    and built with the active feedback of U.S. businesses. This is where you come in.</p>

            <p>The current version of Business.USA.gov has come a long way since its inception.
            With the help of your feedback we have added many new features and content namely
            <a href="http://business.usa.gov/rural-exporting" target="_blank"> Made in Rural America Export and Investment Initiative </a>  Portal,
            <a href="http://business.usa.gov/micro-site/state_resource" target="_blank"> Microsites for States </a>, <a href="http://business.usa.gov/sbir-certification-tool" target="_blank">SBIR Certification Tool</a>,
            <a href="http://business.usa.gov/training-materials-portal" target="_blank"> Training Portal</a>, <a href="http://business.usa.gov/fboopen-widget/fboopen-search" target="_blank">FBO Open Opportunities</a> and lots more,
            please take a <a href="http://business.usa.gov/tour" target="_blank">Tour</a>.
            BusinessUSA has opened new channels of communication such as call centers, email and social media.
            We have started and progressing on integrating state  and local governments,
            as well as related private sector partners. This will help us better deliver the
            information you need on your own terms.</p>

		    <p>To strengthen America's competitiveness in the global economy, businesses will need to be 
		    equipped with the best tools and information available to support innovation and job growth in 
		    the 21st century. BusinessUSA is your front door to all the government has to offer. Help us 
		    make sure it continues to evolve and grow to meet your needs. Sign up for
            <a href="http://business.usa.gov/subscribe?subscribeEmail=Receive+email+updates" target="_blank"> email alerts </a> so you
		    can be the first to find out about changes to BusinessUSA, and use
            <a href="http://help.businessusa.gov/ics/support/ticketnewwizard.asp?style=classic&feedback=true" target="_blank"> feedback features </a> on the
		    website to let us know what you think. BusinessUSA is designed by businesses, for businesses. 
		    Take ownership today.</p>
