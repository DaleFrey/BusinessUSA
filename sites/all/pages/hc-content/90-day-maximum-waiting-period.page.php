Beginning January 1, 2014, individuals who are eligible for employer-provided health 
coverage will not have to wait more than 90 days to begin coverage.  The IRS has 
provided <a href="http://www.irs.gov/pub/irs-drop/n-12-59.pdf">temporary guidance</a> 
on how employers should apply the 90-day rule and is expected to provide more information 
in the near future clarifying these rules.