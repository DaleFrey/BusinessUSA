Beginning January 1, 2013, ACA increases the employee portion of the Medicare Part 
A Hospital Insurance (HI) withholdings by .9% (from 1.45% to 2.35%) on employees 
with incomes of over $200,000 for single filers and $250,000 for married joint filers.  
It is the employer’s obligation to withhold this additional tax, which applies only to 
wages in excess of these thresholds.  The employer portion of the tax will remain 
unchanged at 1.45%.