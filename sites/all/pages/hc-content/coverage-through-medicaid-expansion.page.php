Each state operates a Medicaid program that provides health coverage for lower-income people, 
families and children, the elderly, and people with disabilities. The eligibility rules for Medicaid are 
different for each state, but most states currently offer coverage for adults with children at some 
income level.  In addition, under the Affordable Care Act, states have the option to expand 
Medicaid eligibility to include adults ages 19 – 64 with incomes up to 133% of the Federal Poverty 
Level (about $15,000 per year for an individual, $31,000/year for a family of four).  To learn more 
about your state Medicaid program and other options available to you, use the 
<a href="https://finder.healthcare.gov/">insurance and coverage finder</a> or visit 
<a href="http://www.medicaid.gov/">Medicaid.gov</a>.