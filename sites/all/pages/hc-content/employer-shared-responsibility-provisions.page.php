Beginning in 2015, employers with 50 or more full-time/full-time equivalent employees 
that do not offer affordable health insurance that provides minimum value to their full-time 
employees (and dependents) may be required to pay an assessment if at least one of 
their full-time employees is certified to receive a premium tax credit in an individual 
health insurance Marketplace. A full-time employee is one who is employed an average 
of at least 30 hours per week. The assessment, known as Employer Shared Responsibility, 
will offset part of the cost of the Marketplace premium tax credits. Treasury recently 
issued <a href="http://www.irs.gov/pub/irs-drop/n-13-45.PDF">transitional relief</a> 
to employers covered by these rules indicating that no shared responsibility payments will 
apply until 2015. You can refer to the 
<a href="http://www.irs.gov/pub/newsroom/reg-138006-12.pdf">Proposed Regulations</a> 
for more information about these provisions. To determine if you have 50 or more full-time or 
full-time equivalent employees and are therefore covered by the Employer Shared Responsibility 
rules, 
