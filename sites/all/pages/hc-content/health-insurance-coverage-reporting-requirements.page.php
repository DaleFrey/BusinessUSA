Beginning in 2015, the Affordable Care Act provides for new information reporting 
by employers that sponsor self-insured plans.  (Section 6055 rules). Self-funded 
employers, issuers, and other parties that provide health coverage must submit 
these new reports to the IRS detailing information for each covered individual. 
The first of these reports must be filed in 2016.  On September 5, 2013, the U.S. 
Department of Treasury issued Proposed Regulations that provide further 
guidance about this requirement and invites stakeholders to submit comments 
on these proposed rules through early November 2013. The public comments 
will be taken into account in developing final reporting rules. To review the 
proposed Section 6055 rules, 
