Starting in 2014, the Individual Shared Responsibility provision of the Affordable Care Act means that each individual must 
have basic health insurance coverage (known as minimum essential coverage), qualify for an exemption, or  pay a fee 
when filing a federal income tax return. Individuals will not have to pay the fee (also known as an individual shared 
responsibility payment or individual mandate) if coverage is unaffordable, if they spend less than three consecutive months 
without coverage, or if they qualify for an exemption for several other reasons, including hardship and religious beliefs. The 
fee in 2014 is calculated one of two ways. You'll pay whichever of these amounts is higher:<br/>
<br/>
<ul>
    <li>
        1% of your yearly household income. (Only the amount of income above the tax filing threshold, $10,150 for an 
        individual, is used to calculate the payment.) The maximum payment is the national average yearly premium for a 
        bronze plan.
    </li>
    <li>
        $95 per person for the year ($47.50 per child under 18). The maximum payment per family using this method is $285.
    </li>
</ul>
<br/>
For more information about the Individual Shared Responsibility requirements and exemptions that may apply, refer to this 
Fact Sheet from the U.S. Department of Treasury as well as these 
<a href="http://www.irs.gov/uac/Questions-and-Answers-on-the-Individual-Shared-Responsibility-Provision">Q&As</a> 
from IRS. 
