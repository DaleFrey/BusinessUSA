For plan years beginning on or after January 2013, the maximum amount an 
employee may elect to contribute to health care flexible spending arrangements 
(FSAs) for any year will be capped at $2500, subject to cost-of-living adjustments.  
Note that the limit only applies to elective employee contributions and does not 
extend to employer contributions.  To learn more about FSA Contributions, as 
well as what is excluded from the cap, refer to this 
<a href="http://www.irs.gov/pub/irs-drop/n-12-40.pdf">document</a> provided by the IRS.