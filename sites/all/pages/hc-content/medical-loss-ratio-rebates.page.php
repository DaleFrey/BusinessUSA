Under the ACA, insurance companies must spend at least 80% of premium dollars 
on medical care rather than administrative costs.  Insurers who do not meet this 
ratio are required to provide rebates to their policyholders, which is typically an 
employer who provides a group health plan.  Employers who receive these 
<a href="http://www.dol.gov/ebsa/newsroom/tr11-04.html#.UPBjaeRlH_8">
premium rebates</a> must determine whether the rebates constitute plan 
assets.  If treated as a plan asset, employers have discretion to determine a 
reasonable and fair allocation of the rebate.  For more information on the federal 
tax treatment of Medical Loss Ratio rebates, refer to 
<a href="http://www.irs.gov/uac/Medical-Loss-Ratio-(MLR)-FAQs">IRS's FAQs</a>.