Beginning January 1, 2013, a 3.8% tax will be assessed on 
<a href="http://www.irs.gov/uac/Newsroom/Net-Investment-Income-Tax-FAQs">
net investment incomesuch</a> as taxable capital gains, dividends, rents, royalties, 
and interest for taxpayers with Modified Adjusted Gross Income (MAGI) over 
$200,000 for single filers and $250,000 for married joint filers.  Common types of 
income that are not investment income are wages, unemployment compensation, 
operating income from a non-passive business, Social Security Benefits, alimony, 
tax-exempt interest, and self-employment income.