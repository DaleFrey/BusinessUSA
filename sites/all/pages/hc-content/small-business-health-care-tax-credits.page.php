The small business Health Care Tax Credit helps small employers afford the cost 
of health care coverage for their employees and is specifically targeted for those 
employers with low- and moderate-income workers. The credit is designed to 
encourage small employers to offer health insurance coverage for the first time or 
maintain coverage they already have. Since 2010, businesses that have fewer 
than 25 full-time equivalent employees (FTEs), pay average annual wages below 
$50,000, and that contribute 50% or more toward employees’ self-only health 
insurance premiums may qualify for a 
<a href="https://www.healthcare.gov/will-i-qualify-for-small-business-health-care-tax-credits/">
small business tax credit</a> of up to 35% to 
help offset the costs of insurance. In 2014, this tax credit goes up to 50% and is 
available to qualified small employers that participate in the 
<a href="https://www.healthcare.gov/marketplace/shop/">Small Business Health 
Options Program (SHOP)</a>. Eligible small employers can claim the current credit 
through 2013, and the enhanced credit can be claimed for any two consecutive 
taxable years beginning in 2014 through the SHOP. To calculate your FTEs and 
average annual wages for the purposes of this credit, refer to this 
<a href="http://www.irs.gov/uac/Small-Business-Health-Care-Tax-Credit-Questions-and-Answers:-Determining-FTEs-and-Average-Annual-Wages">
Q&A </a> from IRS. You can also use this 
<a href="http://www.taxpayeradvocate.irs.gov/calculator/SBHCTC.htm">Small Business Health Care Credit Estimator</a> 
to help you find out whether you're eligible for the credit and how much you might receive.