Employers are required to provide employees with a standard “Summary of Benefits and Coverage” form 
explaining what their plan covers and what it costs.  The purpose of the SBC form is to help employees 
better understand and evaluate their health insurance options.  Penalties may be imposed for 
non-compliance.  For more information, refer to this 
<a href="http://www.dol.gov/ebsa/pdf/CorrectedSampleCompletedSBC.pdf">completed sample of the SBC 
form</a> from the U.S. Department of Labor.